          <h4>A Quick Story About How I Became A Republican</h4>
          <h5><small>from Marco Rubio</small></h5>
          <article>
            <p>Here I’m not going to tell you all the reasons I’m a Republican. I’m going to tell you the story of how I first started calling myself a Republican.</p>

            <p>My interest in politics began around the time we moved to Vegas, and by 1980 politics was a preoccupation second only to football. Two events had captured my attention that year: Senator Edward Kennedy’s challenge to President Carter for the Democratic presidential nomination and the Iran hostage crisis. I was a Kennedy supporter.</p>

            <p>With rapt attention I watched the Democratic convention in New York, and was crushed by the outcome of what seemed an excruciatingly slow delegate count that gave the nomination to President Carter. I was inspired by Senator Kennedy’s concession speech.</p>

            <p>My grandfather didn’t admire either of them. Ronald Reagan was his man. He despised President Carter because of the Iran hostage crisis, a humiliation Papá seemed to feel personally. America must be a strong country, he constantly preached, or the world would succumb to darkness, and a strong country requires a strong leader.</p>

            <p>He thought the world didn’t respect or fear Carter. He was weak, he said, and other countries preyed on his weakness. That’s why the Soviets had invaded Afghanistan and the Iranians had seized our embassy. He blamed the failed attempt to rescue the hostages on cuts to defense spending Carter had made.</p>

            <p>Ronald Reagan would restore our strength, he assured me. He would confront communism. Our allies would follow him and our enemies would respect him.</p>

            <p>When Reagan was elected and Iran released our hostages on his inauguration, Papá made certain to point out to me that it confirmed everything he had been telling me. Reagan had barely been sworn into office, and our enemies were already capitulating to him. Reagan’s election and my grandfather’s allegiance to him were defining influences on me politically.</p>

            <p>I’ve been a Republican ever since.</p>

            <p>More than just help me develop a political identity, my grandfather instilled in me the importance of strong leadership and conviction. He urged me to study and learn but, more important, to do something useful with the knowledge I acquired.</p>

            <p>I wrote a paper in the fifth grade praising President Reagan for restoring the U.S. military after it had been demoralized and allowed to decay in the years before his presidency. I recently found it in a red suitcase that had belonged to my grandfather, and still contains some of his possessions.</p>

            <p>Papá was an unwavering supporter of President Reagan for the remainder of his life. He loved Reagan’s anti-Soviet and pro-democracy rhetoric, and he staunchly defended the more controversial Reagan policies. I particularly remember his outspoken support for Reagan’s development of the MX missile, and support for the Contras in Nicaragua and the government of El Salvador.</p>

            <p>(this story first appeared in <a href="http://www.amazon.com/American-Son-Memoir-Marco-Rubio/dp/1595231013/" target="_blank">An American Son</a> by Senator Marco Rubio)</p>
